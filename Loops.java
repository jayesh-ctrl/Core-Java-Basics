public class Loops{
    public static void main(String []args)
    {
        for(int i=1;i<=100;i++)
        {
            System.out.println(i);
        }

        int j=1;
        while(j<=100)
        {
            System.out.println(j);
            j++;

        }

        int k=0;
        do{
            System.out.println(k);
            k++;
        }while(k<100);
     }
}